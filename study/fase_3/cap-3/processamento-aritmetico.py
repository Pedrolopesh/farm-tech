import numpy as np

a = np.array([1, 2, 3])
b = np.array([4, 5, 6])
# Adição
c = a + b
# Subtração
d = a - b
# Multiplicação
e = a * b
# Divisão
f = a / b
print(c, d, e, f)
# Saída: [5 7 9] [-3 -3 -3] [4 10 18] [0.25 0.4 0.5]

a = np.array([1, 2, 3])

# Seno
b = np.sin(a)

# Cosseno
c = np.cos(a)

# Logaritmo
d = np.log(a)

# Exponencial
e = np.exp(a)

# Raiz Quadrada
f = np.sqrt(a)

print(b, c, d, e, f)

